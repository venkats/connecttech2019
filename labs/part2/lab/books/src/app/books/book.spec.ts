import { Book } from './book';

describe('Book', () => {
  it('create a Book', () => {
    const book = new Book('title', 'author', 2019, 10);
    
    expect(book.title).toEqual('title');
    expect(book.author).toEqual('author');
    expect(book.year).toEqual(2019);
    expect(book.quantity).toEqual(10);
  });
});
